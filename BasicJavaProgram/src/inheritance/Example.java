package inheritance;
class Demo1{
	int num1=20;
}
class Demo2{
	int num2=30;
}
class Demo3 {
	int num3=40;
}

public class Example {

	public static void main(String[] args) {
		Demo1 D1=new Demo1();
		System.out.println(D1.num1);
		Demo2 D2=new Demo2();
		System.out.println(D2.num2);
		Demo3 D3=new Demo3();
		System.out.println(D3.num3);
		

	}

}
