package condition;

public class Swap {

	public static void main(String[] args) {
		int a=20,b=40;
		int temp;//taking temporary variable for swap anumber
		System.out.println(a);
		System.out.println(b);
		
		System.out.println("Swap number Is Below");
		temp=a;//a value has been store in temp=20
		a=b;//b value store is been a=40
		b=temp;//temp value is been store in b=20
		System.out.println("swap number of a is"+a);
		System.out.println("swap number of a is"+b);

	}

}
