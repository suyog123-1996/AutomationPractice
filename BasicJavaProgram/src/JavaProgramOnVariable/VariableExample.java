package JavaProgramOnVariable;

public class VariableExample {

	public static void main(String[] args) {
		System.out.println("Program Start");
		//declaraion of variable= datatype variable name;
		byte abc;
		short pqr;			
		int stu;
		long xyz;
		float suv;
		double xuv;
		char trx;
		boolean huv;
		//initilization of variable= variable name = value;
		abc=20;
		pqr=-30;
		stu=2222;
		xyz=-555;//in practicle use byte and short datatype is not used mostly ,Remaining 6 used particularly;
		suv=2.35f;//if you are writing in a decimal point then use f after it eg;='2.35f' 555.60f; 
		xuv=589564;
		trx ='A';//u can use any character like A to Z letter or any symbol'a' 'B'  '/' and so on;
		huv=false;//boolean has only two values true or false;
		System.out.println("abc="+abc);
		System.out.println("pqr="+pqr);
		System.out.println("stu="+stu);
		System.out.println("xyz="+xyz);
		System.out.println("suv="+suv);
		System.out.println("xuv="+xuv);
		System.out.println("trx="+trx);
		System.out.println("huv="+huv);
		System.out.println("Program Ends");
		
		

	}

}
